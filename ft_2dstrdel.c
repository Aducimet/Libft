#include "libft.h"

void	ft_2dstrdel(char ***as)
{
	int		i;

	i = 0;
	if (*as)
	{
		while ((*as)[i])
			ft_strdel(&(*as)[i++]);
		free(*as);
		*as = NULL;
	}
}
